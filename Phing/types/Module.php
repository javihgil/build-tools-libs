require_once "lib/autoload.php";

/**
 * Class Module
 *
 * @author  Javi H. Gil <https://github.com/javihgil>
 */
class Module extends \DataType
{
    /**
     * @var string
     */
    protected $name;

    /**
     * @param string $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}
